        <h3 class="block-faqs-listado-faq-titular titular">¿Cómo se calcula el precio de una caja?</h3>

        <div class="block-faqs-listado-faq-texto rte">
            <p>Puedes calcular tu envío desde la <a href="/">calculadora</a> de nuestra web.</p>

<ul>
	<li style="margin-bottom: 11px; text-align: justify;"><span style="line-height:108%"><font face="Montserrat, sans-serif"><span style="font-size:10pt">Para envíos marítimos la medida es en pies cúbicos. Se toma en cuenta solo el tamaño de su caja a enviar (alto, largo, ancho). Las medidas deben estar en pulgadas y la fórmula es la siguiente: Largo x alto x ancho / 1728. El resultado será en pies cúbicos y lo debes multiplicar por la tarifa de tu zona.</span></font></span></li>
	<li style="margin-bottom: 11px; text-align: justify;"><span style="line-height:108%"><font face="Montserrat, sans-serif"><span style="font-size:10pt">Para envíos aéreos se toma en cuenta lo que sea mayor entre peso (en libras) y peso volumétrico (tamaño) de su caja a enviar. La fórmula para calcular el peso-volumétrico es la siguiente: Largo x alto x ancho / 166. </span></font></span></li>
</ul>

<p style="margin-bottom:11px; text-align:justify"><span style="line-height:108%"><font face="Montserrat, sans-serif"><span style="font-size:10pt">Disponemos también de una <a href="/">calculadora de envíos</a>.</span></font></span></p>
