        <h3 class="block-faqs-listado-faq-titular titular">¿Mi encomienda va asegurada?</h3>

        <div class="block-faqs-listado-faq-texto rte">
            <p style="margin-bottom:11px; text-align:justify"><span style="line-height:108%"><font face="Montserrat, sans-serif"><span style="font-size:10pt">Disponemos de seguro, es recomendado para artículos electrónicos, pero no obligatorio. </span></font></span></p>

<p style="margin-bottom:11px; text-align:justify"><span style="line-height:108%"><font face="Montserrat, sans-serif"><span style="font-size:10pt">El seguro es únicamente para extravío, pérdida o robo. Es de un 5% del valor declarado para artículos electrónicos y un 3% para los no electrónicos, de los cuales la tarifa mínima es $5 (es decir, si el porcentaje da menos de $5 se cobran $5). </span></font></span></p>

<p style="margin-bottom:11px; text-align:justify"><span style="line-height:108%"><font face="Montserrat, sans-serif"><span style="font-size:10pt">El seguro cubre el 75% del valor de la factura (deducible de 25%). De no querer asegurar, debe seleccionar al momento de solicitar el reempaque: "Declino adquirir póliza de seguro" y la empresa en ese caso ofrece un seguro de cortesía por un máximo de $50.</span></font></span></p>
