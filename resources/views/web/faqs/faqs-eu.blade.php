        <h3 class="block-faqs-listado-faq-titular titular">¿Cuándo tengo que realizar el pago?</h3>

        <div class="block-faqs-listado-faq-texto rte">
            <p>La factura se emitirá directa a tu casillero una vez el paquete sea pesado y medido en cualquiera de nuestras oficinas del territorio español. Si eliges la opción de reempaque, la factura se emitirá una vez que tu paquete esté listo para enviar, ya sea vía aérea o marítima.</p>

<p>Ningún paquete saldrá de nuestro almacén si no ha sido pagado, por lo que es importante que no olvides reportar el pago desde casillero.</p>
        </div>
    </article>
